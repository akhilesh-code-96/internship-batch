package module2;

public class SwitchCase {

    public static void main(String[] args) {

        int number = 14;

        switch (number) {
            case 13:
                System.out.println("Execute 13");
                break;
            case 16:
                System.out.println("Execute 14");
                break;
            case 15:
                System.out.println("Execute 15");
                break;
            default:
                System.out.println("Default is executed");
        }

    }
}
